public void buscarCliente() {
        String titulos4[] = {"IdCliente", "Apellidos", "Nombres", "Cedula", "Correo", "Telefono","Direccion"};
        dtmC.setColumnIdentifiers(titulos4);

        clientes cli = new clientes();
        
        busqueda = txtBusqueda.getText();
        
        if (rbtnIdCliente.isSelected()) {
            criterio = "IDCLIENTE";
        }else if (rbtnApellido.isSelected()) {
            criterio = "apellidos";
        } else if (rbtnNombre.isSelected()) {
            criterio = "nombres";
        } else if (rbtnCedula.isSelected()) {
            criterio = "cedula";
        }
        try {
            rs = cli.listarClientesPorParametro1(criterio, busqueda);
            boolean encuentra = false;
            
            String datos[] = new String[7];
            //Borrar registros de dtm existes
            int f, i;
            f = dtmC.getRowCount();
            if (f > 0) {
                for (i = 0; i < f; i++) {
                    dtmC.removeRow(0);
                }
            }
            while(rs.next()) {
                datos[0] = (String) rs.getString(1);
                datos[1] = (String) rs.getString(2);
                datos[2] = (String) rs.getString(3);
                datos[3] = (String) rs.getString(4);
                datos[4] = (String) rs.getString(5);
                datos[5] = (String) rs.getString(6);
                datos[6] = (String) rs.getString(7);

                dtmC.addRow(datos);
                encuentra = true;
            
            f = dtmC.getRowCount();
            lblTotalRegistros.setText(String.valueOf(f));
            }
            if (encuentra = false) {
                JOptionPane.showMessageDialog(null, "No se encuentra!");
            }

        } catch (Exception ex) {
            ex.printStackTrace();
        }
        
        tDatoscliente.setModel(dtmC);
    }
